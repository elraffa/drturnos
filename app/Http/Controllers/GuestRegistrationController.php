    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'name' => 'required',
            'last_name' => 'required',
            'email' => 'required|unique:guests,email',
            'phone_number' => 'required',
        ]);

        $guest = new Guest();
        $guest->name = $validatedData['name'];
        $guest->last_name = $validatedData['last_name'];
        $guest->email = $validatedData['email'];
        $guest->phone_number = $validatedData['phone_number'];
        $guest->save();

        return redirect('guest/guest-success')->with('success', 'Gracias por registrarte.');
    }

